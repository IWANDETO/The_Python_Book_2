#!/usr/bin/python3
#\t is used to to tab in which character coming after it will be spaced into a new tab
print("Programming Languages: \tC\tPython\tJavaScript")

#\n is used to create a new line in which character coming after it will be printed in  a new line
print("Programming Languages: \nC\nPython\nJavaScript")

#The tab and new line function can be used together as demonstrated below
print("The Programming Languages I want to Learn are:\n\tPython\n\tC\n\tJavaScript")